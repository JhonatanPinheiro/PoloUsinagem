function abrir_ajuda_pop(){
  document.getElementById('ajuda_pop').style.display = 'block'
  document.getElementById('icone_abrir_ajuda').style.display = 'none';
  document.getElementById('icone_fechar_ajuda').style.display = '';

}

 function fechar_ajuda_pop(){
     document.getElementById('ajuda_pop').style.display = 'none'
     document.getElementById('icone_abrir_ajuda').style.display = ''
     document.getElementById('icone_fechar_ajuda').style.display = 'none';

 }
